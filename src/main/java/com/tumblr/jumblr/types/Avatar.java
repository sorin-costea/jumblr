package com.tumblr.jumblr.types;

/**
 * This class represents an avatar entry in blog descriptions
 *
 * @author jc
 */
public class Avatar {

  private int width, height;
  private String url;

  /**
   * @return the width
   */
  public int getWidth() {
    return width;
  }

  /**
   * @return the height
   */
  public int getHeight() {
    return height;
  }

  /**
   * @return the url
   */
  public String getUrl() {
    return url;
  }

}
